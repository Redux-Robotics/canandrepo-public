#[test]
fn compare_against_java() {
    for js in TEST_DATA {
        // lifecycle, devId, batchId, revision, productId
        let (lifecycle, device, batch, rev, product, serial_bin, serial_u64) =
            (js.0, js.1, js.2, js.3, js.4, js.5, js.6);

        let from_bin = SerialNumer::new(serial_bin);
        let serial = SerialNumer::from(serial_u64);

        assert_eq!(from_bin, serial, "{:?} != {:?}", from_bin, serial);
        assert!(serial.check_crc(), "crc does not check for {:?}", from_bin);

        assert_eq!(serial.lifecycle_flag() as u8, lifecycle, "{:x?}", serial);
        assert_eq!(
            serial.lifecycle_flag(),
            LifecycleFlag::try_from(lifecycle).unwrap(),
            "{:x?}",
            serial
        );
        assert_eq!(serial.device_id(), device);
        assert_eq!(serial.batch_id(), batch);
        assert_eq!(serial.revision_id(), rev);
        assert_eq!(Into::<u8>::into(serial.product_id()), product);
        assert_eq!(serial.product_id(), ProductId::from(product));
    }
}

